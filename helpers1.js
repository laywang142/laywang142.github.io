// create slider 1
var slider = document.getElementById('slider-1');

noUiSlider.create(slider, {
    start: [1, 8],
    connect: true,
    range: {
        'min': 1,
        'max': 10
    },

    step:1,

    // Show a scale with the slider
    pips: {
      mode: 'steps',
      stepped: true,
      density: 10
  }
});

